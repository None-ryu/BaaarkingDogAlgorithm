
# https://www.acmicpc.net/problem/2446
N = int(input())

for i in range(N):
    #print(i, ((N-i)*2-1))
    print(" "*(i) + "*"*((N-i)*2-1))
for i in range(N-1):
    #print(i, (N-i+2))
    print(" "*(N-i-2) + "*"*(2*(i+2)-1))
